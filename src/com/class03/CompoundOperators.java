package com.class03;

public class CompoundOperators {
	public static void main(String[] args) {
		
		int num=100;
		num=num+100;
		System.out.println(num);
		
		num=num-50;
		System.out.println(num);
		
		int num1=100;
		num1+=100;
		System.out.println(num1);
		
		num1-=50;
		System.out.println(num1);
		
		int a=100;
		a+=500;
		System.out.println(a);
		a-=67;
		System.out.println(a);
		
		int cakepiece=11;
		cakepiece/=4;
		System.out.println(cakepiece);
		
		int cake=25;
		cake&=7;
		System.out.println(cake); //should be 4
		
	
		
		
	}

}
