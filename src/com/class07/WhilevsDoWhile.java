package com.class07;

public class WhilevsDoWhile {

	public static void main(String[] args) {
		int num=1;
		while(num<=5) {
			System.out.println(num);
			num++;
		}
		System.out.println("-----------Do While-------------------");
		
		int num1=6;//// change it to 6
		
		do {
			System.out.println(num1);
			num1++;
			
		}while(num1<=5);
			
		}
		
		//while and Do while differences is while first checks than executes ***Do while executes than checks 
		
		
	}
	
	


